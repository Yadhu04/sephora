class SimilarProduct {
  final String id;
  final String handle;
  final String image;
  final String brand;
  final String title;        
  final String variantTitle;
  final double price;
  final double rating;
  final int reviewCount;

  SimilarProduct({
    required this.id,
    required this.handle,
    required this.image,
    required this.brand,
    required this.title,
    required this.variantTitle,
    required this.price,
    required this.rating,
    required this.reviewCount,
  });
}
